# Задача:
# Написать программу, которая:
# 1. Запрашивает у пользователя количество строк (с проверкой ввода).
# 2. Запрашивает у пользователя сами строки текста.
# 3. Определяет, сколько различных слов содержится в тексте, и выводит их количество.
# Примечание: Словом считается последовательность непробельных символов, разделенных одним или большим количеством пробелов или символами конца строки.

while True:
    try:
        num_lines = int(input("Введите количество строк: "))  # Запрашиваем количество строк
        if num_lines > 0:  # Проверка, что число строк больше 0
            break  # Если проверка пройдена, выходим из цикла
        else:
            print("Введите положительное число строк.")  # Если число меньше или равно 0, выводим сообщение об ошибке
    except ValueError:
        print("Введите корректное целое число.")  # Если ввод не является целым числом, выводим сообщение об ошибке
text = "" # Инициализируем пустую строку для хранения всего текста
for i in range(num_lines):
    line = input(f"Введите строку {i + 1}: ")  # Запрашиваем строку текста
    text += line + " "  # Добавляем строку в общую текстовую переменную с пробелом для разделения слов
words = text.split() # Разделяем текст на слова с использованием split(), который разбивает строку по пробелам
unique_words = set(words) # Преобразуем список слов в множество для удаления дубликатов и считаем уникальные слова
print(f"Количество различных слов в тексте: {len(unique_words)}") # Выводим количество уникальных слов